function colorRed() {
  let items = document.getElementsByTagName("p");

  if (items.length > 0) {
    for (let i = 0; i < items.length; i++) {
      items[i].style.color = "red";
    }
  }

  items = document.getElementsByTagName("h1");

  if (items.length > 0) {
    for (let i = 0; i < items.length; i++) {
      items[i].style.color = "red";
    }
  }

  items = document.getElementsByTagName("h2");

  if (items.length > 0) {
    for (let i = 0; i < items.length; i++) {
      items[i].style.color = "red";
    }
  }

  items = document.getElementsByTagName("h3");

  if (items.length > 0) {
    for (let i = 0; i < items.length; i++) {
      items[i].style.color = "red";
    }
  }

  items = document.getElementsByTagName("li");

  if (items.length > 0) {
    for (let i = 0; i < items.length; i++) {
      items[i].style.color = "red";
    }
  }

  items = document.getElementsByTagName("label");

  if (items.length > 0) {
    for (let i = 0; i < items.length; i++) {
      items[i].style.color = "red";
    }
  }
}

